package com.java.lessons;

public class If {

    public static void main(String[] args) {
        int myInt = 10;
        if (myInt < 5) {
            System.out.println("myInt is less than 5");
        } else if (myInt == 5) {
            System.out.println("myInt equals 5");
        } else {
            System.out.println("myInt is greater than 5");
        }
    }
}
